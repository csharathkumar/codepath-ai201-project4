@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute; 50 per hour; 100 per day")
def submit():
    """
    POST /submit
    Body: { "text": str, "creator_id"?: str, "content_id"?: str }
    Accepts "content" as an alias for "text" for backward compatibility.
    Returns: classification result + transparency label
    """
    body = request.get_json(silent=True) or {}
    # Accept "text" (spec field name) or "content" (alias)
    content = (body.get("text") or body.get("content") or "").strip()

    if not content:
        return jsonify({"error": "Field 'text' is required and must not be empty."}), 400
    if len(content) > 20_000:
        return jsonify({"error": "Content exceeds 20,000 character limit."}), 400

    content_id = body.get("content_id") or str(uuid.uuid4())
    creator_id = body.get("creator_id") or "anonymous"

    # Run both signals
    llm_result = llm_signal(content)
    stylo_result = stylo_signal(content)

    llm_ai_prob = llm_result["ai_probability"]
    stylo_ai_prob = stylo_result["ai_probability"]

    # Blend and score
    final_score = blend_signals(llm_ai_prob, stylo_ai_prob)
    lv = label_variant(final_score)
    label = make_label(lv, final_score)

    # Verdict (internal) and attribution (user-facing alias)
    if lv == "high_ai":
        verdict = "ai"
        attribution = "likely_ai"
    elif lv == "high_human":
        verdict = "human"
        attribution = "likely_human"
    else:
        verdict = "uncertain"
        attribution = "uncertain"

    # Write to audit log
    db = get_db()
    write_decision(db, content_id, creator_id, verdict, final_score, llm_ai_prob, stylo_ai_prob, lv)

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,      # user-facing: "likely_ai" | "likely_human" | "uncertain"
        "verdict": verdict,              # internal alias kept for compatibility
        "confidence": final_score,
        "signals": {
            "llm": {
                "verdict": llm_result["verdict"],
                "ai_probability": llm_ai_prob,
                "reasoning": llm_result["reasoning"],
                "weight": LLM_WEIGHT,
            },
            "stylometric": {
                "ai_probability": stylo_ai_prob,
                "sub_metrics": stylo_result["sub_metrics"],
                "weight": STYLO_WEIGHT,
            },
        },
        "label": label,
        "appeal_endpoint": "/appeal",
    }), 200


@app.route("/appeal", methods=["POST"])
@limiter.limit("5 per hour")
def appeal():
    """
    POST /appeal
    Body: { "content_id": str, "creator_reasoning": str, "creator_id"?: str }
    Accepts "reason" as an alias for "creator_reasoning".
    """
    body = request.get_json(silent=True) or {}
    content_id = body.get("content_id", "").strip()
    creator_id = body.get("creator_id", "").strip()
    # Accept "creator_reasoning" (spec field) or "reason" (alias)
    reason = (body.get("creator_reasoning") or body.get("reason") or "").strip()

    if not content_id:
        return jsonify({"error": "'content_id' is required."}), 400
    if not reason:
        return jsonify({"error": "'creator_reasoning' is required — please explain why you believe the classification is incorrect."}), 400

    db = get_db()
    row = db.execute(
        "SELECT * FROM submissions WHERE content_id = ?", (content_id,)
    ).fetchone()

    if row is None:
        return jsonify({"error": "No submission found with that content_id."}), 404

    if row["status"] == "under_review":
        return jsonify({
            "message": "This content is already under review.",
            "content_id": content_id,
            "status": "under_review",
        }), 200

    effective_creator = creator_id or row["creator_id"]
    write_appeal(db, content_id, effective_creator, reason)

    return jsonify({
        "message": "Your appeal has been received and logged. A human reviewer will assess the original decision and your reasoning.",
        "content_id": content_id,
        "status": "under_review",
        "next_steps": "Appeals are reviewed within 5 business days. You will be notified via the platform when a decision is made.",
    }), 200


@app.route("/log", methods=["GET"])
@limiter.limit("30 per minute")
def audit_log():
    """
    GET /log?limit=20&offset=0
    Returns recent audit log entries.
    """
    try:
        limit = min(int(request.args.get("limit", 20)), 100)
        offset = int(request.args.get("offset", 0))
    except ValueError:
        return jsonify({"error": "limit and offset must be integers"}), 400

    db = get_db()
    rows = db.execute(
        """SELECT * FROM audit_log ORDER BY id DESC LIMIT ? OFFSET ?""",
        (limit, offset),
    ).fetchall()

    total = db.execute("SELECT COUNT(*) FROM audit_log").fetchone()[0]

    entries = [dict(row) for row in rows]
    return jsonify({"total": total, "entries": entries}), 200


